
public class TestLocalVariable {
	
	public static void main(String[] args) {
		boolean b = true;
		byte y = 1;
		char c = 'A';
		short s = 23;
		int i = 3 ;
		long l = 525;
		float f = 2.1f ;
		double d = 2.2d ;
		String r = "Teschsum" ;
		
		System.out.println(" value for boolean ="+b);
		System.out.println(" value for byet ="+y);
		System.out.println(" value for char ="+c);
		System.out.println(" value for int ="+i);
		System.out.println(" value for short ="+s);
		System.out.println(" value for long ="+l);
		System.out.println(" value for float ="+f);
		System.out.println(" value for double ="+d);
		System.out.println(" value for String ="+r);
	
	
	}

}
